import { Audio } from 'expo-av';

export class Piano {
	#playNotes: { [id: string]: Audio.Sound } = {};
	constructor() {}

	loadNotes = () => {
		return Promise.all(Object.keys(notes).map((key: string) => this.#loadNote(key, notes[key])));
	};

	playRandomNode = () => {
		const notesKeys = Object.keys(this.playNote);
		const randomIndex = randomIntFromInterval(0, notesKeys.length);
		const randomNode = notesKeys[randomIndex];
		console.log('randomNode', randomNode);
		if (randomNode) {
			return this.#playNotes[randomNode].replayAsync();
		} else {
			return () => {};
		}
	};

	playNote = (name: string) => {
		const noteToPlay = this.#playNotes[name];

		if (noteToPlay) {
			return this.#playNotes[name].playFromPositionAsync(0);
		} else {
			() => {};
		}
	};

	playNotes = (names: Array<string>) => {
		return this.playNote(names[0]);
		return names.map(name => this.playNote(name));
	};

	#loadNote = async (name: string, asset: any) => {
		return new Promise(async (resolve, reject) => {
			try {
				const { sound } = await Audio.Sound.createAsync(asset);
				this.#playNotes[name] = sound;
				resolve('finish');
			} catch (error) {
				reject(error);
			}
		});
	};
}

const randomIntFromInterval = (min: number, max: number) => {
	return Math.floor(Math.random() * (max - min + 1) + min);
};

export const loadNoteAsync: (asset: any) => Promise<Audio.Sound> = async (asset: any) => {
	return new Promise(async (resolve, reject) => {
		try {
			const { sound } = await Audio.Sound.createAsync(asset);

			return resolve(sound);
		} catch (error) {
			reject(error);
		}
	});
};
